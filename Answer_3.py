#Use of Bitwise Operators.
a = 56
b = 10

#Use of Bitwise AND(&) operator.
print("A. a & b =", a & b)

#Use of Bitwise OR(|) operator.
print("B. a | b =", a | b)
 
#Use of Bitwise XOR(^) operator.
print("C. a ^ b =", a ^ b)

#Use of shift(left) operator.
print("D. a << 2 = ", a << 2)

#Use of shift(both) operator.
print("E. a >> 2 = ", a >> 2," and ", "b >> 4 = ", b >> 4 )
